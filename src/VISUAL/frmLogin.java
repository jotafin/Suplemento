    public void Logar()
    {
        String sql = "Select *from login where usuario = ? and senha = ?";
        
        try{
            pst = con.prepareStatement(sql);  //chamando a conexão
            pst.setString(1, txtUsuario.getText() );
            pst.setString(2, txtSenha.getText());
            
            rs = pst.executeQuery();
            
            //Verificar se tem o proximo registro
            if(rs.next()){
                frmPrincipal frm = new frmPrincipal();
                frm.setVisible(true);
                dispose(); //Fecha a janela depois de logar
            }
            else{
                JOptionPane.showMessageDialog(null, "Usúario e senha inválidos");
        }
        
        }
        // CASO DER ALGUM ERRO
        catch(SQLException error){
            
            JOptionPane.showMessageDialog(null, error);
        }
    
    
    }
    
